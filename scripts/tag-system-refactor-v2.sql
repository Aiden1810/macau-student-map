-- 历史数据批量补全（从旧 tags 映射到新 tag_ids）
update public.shops s
set
  tag_ids = coalesce((
    select array_agg(distinct m.target_tag_id)
    from unnest(coalesce(s.tags, '{}')) t(v)
    join public.tag_migration_map m on m.legacy_tag = t.v
  ), '{}'),
  canonical_tags = coalesce((
    select array_agg(distinct tg.tag_name)
    from unnest(coalesce(s.tags, '{}')) t(v)
    join public.tag_migration_map m on m.legacy_tag = t.v
    join public.tag tg on tg.id = m.target_tag_id
  ), '{}')
where cardinality(coalesce(s.tag_ids, '{}')) = 0;

