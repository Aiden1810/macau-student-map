-- Tag system refactor v2: canonical + synonym + expand + fallback + migration + logs
-- Compatible with existing schema: keeps legacy tags/sub_tags/main_category fields.

begin;

create extension if not exists pgcrypto;

-- 1) tag 表（若已有则兼容）
create table if not exists public.tag (
  id uuid primary key default gen_random_uuid(),
  level1 text not null,
  level2 text not null,
  tag_name text not null,
  is_canonical boolean not null default true,
  status text not null default 'active',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (level1, level2),
  unique (tag_name)
);

alter table public.shops add column if not exists tag_ids uuid[] not null default '{}';
alter table public.shops add column if not exists canonical_tags text[] not null default '{}';

-- 2) 同义词表：支持一词多tag + weight
create table if not exists public.tag_synonym (
  id bigserial primary key,
  synonym text not null,
  tag_id uuid not null references public.tag(id) on delete cascade,
  weight numeric(4,3) not null check (weight > 0 and weight <= 1),
  lang text not null default 'zh',
  source text not null default 'manual',
  created_at timestamptz not null default now(),
  unique (synonym, tag_id)
);

-- 3) 扩展词表
create table if not exists public.query_expand_dict (
  id bigserial primary key,
  query_term text not null,
  expand_term text not null,
  weight numeric(4,3) not null default 0.6,
  created_at timestamptz not null default now(),
  unique (query_term, expand_term)
);

-- 4) 搜索日志闭环
create table if not exists public.search_query_log (
  id bigserial primary key,
  query text not null,
  normalized_query text not null,
  hit boolean not null,
  matched_level text not null check (matched_level in ('exact', 'synonym', 'expanded', 'similar_category')),
  result_count int not null default 0,
  user_anon_id text null,
  searched_at timestamptz not null default now()
);

-- 5) 旧tag迁移映射
create table if not exists public.tag_migration_map (
  id bigserial primary key,
  legacy_tag text not null unique,
  target_tag_id uuid not null references public.tag(id) on delete cascade,
  note text,
  created_at timestamptz not null default now()
);

-- 6) 索引
create index if not exists idx_tag_level1 on public.tag(level1);
create index if not exists idx_tag_tag_name on public.tag(tag_name);
create index if not exists idx_tag_synonym_synonym on public.tag_synonym(synonym);
create index if not exists idx_tag_synonym_tag_id on public.tag_synonym(tag_id);
create index if not exists idx_expand_query_term on public.query_expand_dict(query_term);
create index if not exists idx_search_log_query_time on public.search_query_log(normalized_query, searched_at desc);
create index if not exists idx_search_log_hit_level on public.search_query_log(hit, matched_level, searched_at desc);
create index if not exists idx_shops_tag_ids_gin on public.shops using gin(tag_ids);
create index if not exists idx_shops_canonical_tags_gin on public.shops using gin(canonical_tags);

